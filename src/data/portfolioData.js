// optional helpers (still fine to keep)
export function getEventById(id) {
  return EVENT_PROJECTS.find((item) => item.id === id);
}

export function getAcademicById(id) {
  return ACADEMIC_PROJECTS.find((item) => item.id === id);
}

export function getMarketingById(id) {
  return MARKETING_EXPERIENCES.find((item) => item.id === id);
}

// used by [slug]/[projectId].js
export function getProjectFromCategory(slug, projectId) {
  const category = PROJECT_CATEGORIES[slug];
  if (!category) return null;
  return category.items.find((item) => item.id === projectId) || null;
}

// used by getStaticPaths of detail page
export function getAllProjectsWithSlugs() {
  const result = [];
  Object.entries(PROJECT_CATEGORIES).forEach(([slug, category]) => {
    category.items.forEach((item) => {
      result.push({
        slug,
        id: item.id,
      });
    });
  });
  return result;
}
